def main():
    # entrada
    A = int(input("Primeiro numero: "))
    B = int(input("Segundo numero: "))
    C = int(input("Terceiro numero: "))
    D = int(input("Quarto numero: "))

    # processamento: Matemática + Condicionais + Repetições
    DIFERENCA = (A * B) - (C * D)

    # saída
    print("DIFERENCA =", DIFERENCA)


main()